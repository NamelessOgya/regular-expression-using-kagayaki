#include <stdio.h>
#include <string.h>
#include "nfa.h"

/**
 * 正規表現を「後置表現（逆ポーランド記法）」に変換する処理。
 * 
 * [担当処理]
 * 中置記法（例: "a(b|c)*"）の正規表現を受け取り、演算子がオペランドの後ろに来る
 * 後置表現に変換します。パースの便宜上、連結演算子として明示的に '#' を挿入します。
 * 後置表現にすることで、スタック操作だけで効率よくNFAを構築できるようになります。
 * 
 * [想定I/O]
 * - input:  中置記法の正規表現文字列 (例: "re") (char *)
 * - output: 逆ポーランド記法（後置表現）に変換された静的バッファへのポインタ (char *)
 * 
 * ▼ 記号ごとの理想的な I/O 変換例
 * 1. 【基本・連結】   IN: "ab"       → OUT: "ab#"
 *    (文字が2つ続いた場合、暗黙の連結として明示的に '#' が挿入されます)
 * 
 * 2. 【選択 (OR)】    IN: "a|b"      → OUT: "ab|"
 *    (2文字が処理された「後に」ORが適用される形になります)
 * 
 * 3. 【繰り返し】     IN: "a*"       → OUT: "a*"
 *    (単項演算子なので、文字の直後にそのまま出力されます)
 * 
 * 4. 【カッコ制御】   IN: "a(b|c)"   → OUT: "abc|#"
 *    (カッコ記号自体は出力には一切現れません。bとcのOR `bc|` が作られた後に、
 *     a とその結果のカタマリが連結 `#` されます)
 * 
 * 5. 【複合パターン】 IN: "a(b|c)*d" → OUT: "abc|*#d#"
 *    (a と (b|c)* が `#` で連結され、さらに最後の d が `#` で連結されます)
 */
char*
re2post(char *re)
{
    /* 
     * 【処理待ちの OR記号 (|) の数】
     * "a|b|c" のようにOR記号が出現した回数をカウントします。
     * 逆ポーランド記法ではORの適用は後回しにされる(対象アトムが出力されたから適用される)ため、カッコが閉じられるか文字列が終わるまで
     * 出力を「保留」しておくために必要です。
     */
    int num_alt = 0;

    /* 
     * 【現在のカッコ階層内における、未連結なアトム（カタマリ）の数】
     * 今読んでいる文字が「直前の文字とくっつける（連結する）べきか」を判定するために必要です。
     * これが2以上になった瞬間に「間に連結記号（#）を挟むべきだ！」という合図になります。
     */
    int num_atom = 0;

    /* 
     * 【変換後の逆ポーランド記法を格納する出力先配列】
     * 関数を抜けた後もメイン処理（post2nfa 等）にポインタを安全に渡せるように `static` で宣言されます。
     */
    static char postfix_buffer[8000];

    /* 出力先配列の、現在の書き込み位置（カーソル）を指すポインタ */
    char *dest = postfix_buffer;
    
    /*
     * 【カッコのネスト（入れ子）状態を退避・復元するためのスタックメモリ】
     * 開きカッコ `(` に入るたびに、親階層（より外側のカッコ）で数えてきた
     * `num_alt` と `num_atom` をいったんここに避難（保存）し、カッコ内をゼロから独立してカウントし直すために必要です。
     */
    struct {
        int num_alt;
        int num_atom;
    } parentheses[100];//100 * 2の行列ができる。
    
    /* 
     * 【現在のカッコの深さ (退避スタックの現在のインデックス)】
     * "(((a)))" のように入れ子に入るたびに増え、閉じカッコ `)` で減ります。
     */
    int paren_index = 0;
    
    /* 
     * 【バッファ溢れ（Buffer Overflow）の防止チェック】
     * 変換後の文字列は、最悪の場合「入力文字数の約2倍」の長さに膨れ上がります。
     * 例: "abc" (3文字) → "ab#c#" (5文字) のように、文字の間に大量の連結記号 '#' が追加されるため。
     * そのため、もし入力された正規表現文字列がバッファサイズ(8000)の半分以上あった場合、
     * 確実にバッファを突き破ってしまうため、安全のためにパースを諦めてエラー(NULL)を返します。
     */
    if (strlen(re) >= sizeof(postfix_buffer) / 2) {
        return NULL;
    }
    
    /* 
     * 【文字列を1文字ずつ先頭から読み進めるループ】
     * C言語における文字列の末尾には必ず「番兵」として '\0' (ヌル文字) が付いています。
     * 1. 準備(開始時): 文字列 re の先頭番地を current_char にセットする
     * 2. 条件(継続)  : 今見ている文字番地の中身(*current_char)が、末尾の '\0' でない限りループを続ける
     * 3. 更新(1周後) : 見ている番地を1つ横に進める (current_char++)
     */
    for (char *current_char = re; *current_char != '\0'; current_char++) {
        switch (*current_char) {
        case '(':
            /* 
             * 例: "ab(c)" のように、文字のアトムが2個連続した直後にカッコが来る場合、
             * カッコへ入る前に、すでに並んでいるアトム(aとb)を確実につなぎ合わせるための処理です。
             */
            if (num_atom > 1) {
                num_atom--; //アトムの数を減らす。（連結したので）
                *dest = '#'; //連結演算子を入れる
                dest++; //ポインタを進める(使ったので)
            }

            // ネスト数が限界を超える場合、エラーを返す。
            if (paren_index >= 100) {
                return NULL;
            }
            parentheses[paren_index].num_alt = num_alt; //現在の処理待ち|の数を覚えておく
            parentheses[paren_index].num_atom = num_atom; //現在のアトム数を覚えておく
            paren_index++; //カッコの深さを1増やす
            num_alt = 0; //新しいカッコの中なので、処理待ち|の数を0にする
            num_atom = 0; //新しいカッコの中なので、現在のアトム数を0にする
            break;
            
        case '|':
            if (num_atom == 0) {
                return NULL; /* "a|" や "|b" のような構文エラー */
            }
            /* 
             * 直前のカタマリ同士を先に連結しておきます。
             * 例: "ab|c" の場合、bとcをORするのではなく、(ab) と c をORするため、
             * まず aとb を連結（#）して1つのアトムにします。
             */
            while (num_atom > 1) {
                num_atom--; //アトム数を減らす。連結して一つになるので。
                *dest = '#'; //連結演算子を入れる
                dest++; //ポインタを進める(使ったので)
            }
            num_atom = 0; // 選択肢（OR）の区切りが来たので、現在のアトム数をリセット
            num_alt++;    // 処理待ちのOR記号の数を増やす
            break;
            
        case ')':
            if (paren_index == 0 || num_atom == 0) {
                return NULL; /* カッコの対応がおかしいエラー "("がない OR "("との間にatomが一つもない*/
            }
            /* カッコを閉じる前に、カッコ内の未結合のアトムを全て連結（#）します */
            while (num_atom > 1) {
                num_atom--;
                *dest = '#';
                dest++;
            }
            /* カッコ内に含まれていた処理待ちのOR記号（|）を全て吐き出します */
            for (; num_alt > 0; num_alt--) {
                *dest = '|';
                dest++;
            }
            /* 親（1つ外側のカッコレベル）の状態を復元し、
               たった今閉じたカッコ全体のカタマリを「1つのアトム」としてカウントします */
            paren_index--;
            num_alt = parentheses[paren_index].num_alt;
            num_atom = parentheses[paren_index].num_atom;
            num_atom++;
            break;
            
        case '*':
        case '+':
        case '?': //このようにcaseを3つ並べて書くとORみたいな処理になる。
            if (num_atom == 0) {
                return NULL; /* 演算子の前に文字が無い構文エラー (例: "*a") */
            }
            /* 
             * 繰り返しなどの単項演算子は、直前の1つのアトムにのみ適用されます。
             * そのため連結（#）を待たずに即座に出力します。
             */
            *dest = *current_char; //いずれも直前のatomを繰り返す処理なので、そのままくっ付ければいい。(*と?の違いなどは処理時に考える。)
            dest++;
            break;
            
        default:
            /* 
             * 純粋な文字（オペランド）が来た時の処理です。
             * すでに前にも文字（アトム）があった場合、それは連結されるべきなので
             * 間に黙って連結記号（#）を挟んでから文字を出力します。
             * 例: "ab" → 'a'を出力した後、'b'が来たら間に '#' を挟んで出力するため "ab#" となる。
             */

            // 必要なら結合記号をくっつけ。
            if (num_atom > 1) {
                num_atom--;
                *dest = '#';
                dest++;
            }
            *dest = *current_char; //文字列なのでそのままくっつけ
            dest++; 
            num_atom++;
            break;
        }
    }
    // ループ終了後、閉じカッコが残っていたらエラー
    if (paren_index != 0) {
        return NULL;
    }
    // 最後に残ったアトムを連結
    while (num_atom > 1) {
        num_atom--;
        *dest = '#';
        dest++;
    }
    // 最後に残ったOR記号を吐き出す
    for (; num_alt > 0; num_alt--) {
        *dest = '|';
        dest++;
    }
    *dest = '\0'; //最後に終了記号を置く
    return postfix_buffer;
}
